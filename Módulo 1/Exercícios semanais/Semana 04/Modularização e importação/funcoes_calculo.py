def soma(a, b): #soma 
    n1 = a
    n2 = b
    total = n1+n2
    return total



def sub(a,b): #subtração
    n1 = a
    n2 = b
    total = n1-n2
    return total

def mult(a,b): #multiplicação
    n1 = a
    n2 = b
    total = n1*n2
    return total

def div(a,b): #divisão
    n1 = a
    n2 = b
    total = n1/n2
    return total


